const fs = require('fs')
const path = require('path')
const simpleGit = require('simple-git')
const { loadRules } = require('../lib/rules')
const { globMatch, matchAllPatterns, resolveKbTarget, extractName } = require('../lib/patterns')

const KB_ROOT = 'knowledge'
const CODE_DRIFT_PATH = path.join(KB_ROOT, 'sync/code-drift.md')
const KB_DRIFT_PATH = path.join(KB_ROOT, 'sync/kb-drift.md')
const DRIFT_LOG_DIR = path.join(KB_ROOT, 'sync/drift-log')

function getDriftLogPath() {
  const d = new Date()
  const month = `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}`
  return path.join(DRIFT_LOG_DIR, `${month}.md`)
}

const CODE_DRIFT_HEADER = `<!-- AUTO-GENERATED by kb-mcp — resolve via kb_drift in Claude. Do not delete entries manually. -->

# Code Drift Queue

Code changed without matching KB update. PM/tech lead reviews with Claude — approve (update KB) or reject (revert code) per KB document.

`

const KB_DRIFT_HEADER = `<!-- AUTO-GENERATED by kb-mcp — resolve via kb_drift in Claude. Do not delete entries manually. -->

# KB Drift Queue

KB spec changed — developer reviews with Claude whether code still matches.

`

const DRIFT_LOG_HEADER = `<!-- AUTO-GENERATED audit trail — do not edit manually. -->

# Drift Log

Resolved drift events. Append-only.

`

/**
 * kb_drift — Bidirectional drift detection.
 *
 * Phase 1 (no resolution params):
 *   code→kb: code files changed → upsert entries in sync/code-drift.md
 *            keyed by KB target, tracks all code files + since-commit per file
 *            multiple commits to same file accumulate automatically (git diff since..HEAD)
 *   kb→code: KB files changed → upsert entries in sync/kb-drift.md
 *            keyed by KB file, tracks since-commit
 *
 * Phase 2 resolutions:
 *   summaries:    [{ kb_target, summary }]  PM approved — KB updated
 *   reverted:     [{ code_file }]            PM rejected — code file reverted
 *   kb_confirmed: [{ kb_file }]              developer confirmed code matches
 */
async function runTool({ since = 'last-sync', summaries, reverted, kb_confirmed, remote } = {}) {
  if (summaries && Array.isArray(summaries)) return resolveWithSummaries(summaries)
  if (reverted && Array.isArray(reverted)) return resolveReverted(reverted)
  if (kb_confirmed && Array.isArray(kb_confirmed)) return resolveKbConfirmed(kb_confirmed)
  return detectDrift(since, remote)
}

// ── Phase 1: detect drift ─────────────────────────────────────────────────────

async function detectDrift(since, remote) {
  const mainGit = simpleGit(process.cwd())
  const rules = loadRules(KB_ROOT)

  // Resolve the comparison ref:
  // - explicit SHA/ref → use directly
  // - 'last-sync' → graduated fallback via resolveLastSyncRef
  let ref
  if (since !== 'last-sync') {
    ref = since
  } else {
    ref = await resolveLastSyncRef(mainGit, remote)
  }

  // Get HEAD commit info for "since" tracking
  let headCommit = 'unknown'
  let headDate = new Date().toISOString().split('T')[0]
  try {
    const log = await mainGit.log({ maxCount: 1 })
    if (log.latest) {
      headCommit = log.latest.hash.slice(0, 7)
      headDate = log.latest.date.split('T')[0]
    }
  } catch { /* non-fatal */ }

  let changedEntries = []
  if (ref === null) {
    process.stderr.write('[kb-drift] warning: no sync baseline found — skipping main repo drift detection\n')
  } else {
    try {
      const mainFiles = await getChangedFiles(mainGit, ref)
      changedEntries.push(...mainFiles.map(f => ({ file: f, git: mainGit, localFile: f, commit: headCommit, date: headDate })))
    } catch (e) {
      return { code_entries: 0, kb_entries: 0, error: e.message }
    }
  }

  const submodules = await detectSubmodules()
  for (const sub of submodules) {
    try {
      const subGit = simpleGit(sub.fullPath)
      let subCommit = 'unknown'
      let subDate = headDate
      try {
        const subLog = await subGit.log({ maxCount: 1 })
        if (subLog.latest) {
          subCommit = subLog.latest.hash.slice(0, 7)
          subDate = subLog.latest.date.split('T')[0]
        }
      } catch { /* non-fatal */ }
      let subRef
      try { subRef = await resolveLastSyncRef(subGit, remote) } catch { subRef = null }
      if (subRef === null) {
        process.stderr.write(`[kb-drift] warning: no sync baseline for submodule ${sub.path} — skipping\n`)
        continue
      }
      const subFiles = await getChangedFiles(subGit, subRef)
      changedEntries.push(...subFiles.map(f => ({
        file: `${sub.path}/${f}`,
        git: subGit,
        localFile: f,
        commit: subCommit,
        date: subDate,
        isShared: sub.isShared
      })))
    } catch { /* submodule may not have enough history */ }
  }

  const patterns = rules.getCodePathPatterns()
  let codeEntriesWritten = 0
  let kbEntriesWritten = 0

  for (const entry of changedEntries) {
    const { file, commit, date, isShared } = entry

    if (isKbContentFile(file)) {
      // kb→code: KB file changed — find all associated code areas
      const relative = file.replace(/^knowledge\//, '')
      const codePaths = reverseMapKbTarget(relative, patterns)
      if (codePaths.length > 0) {
        const wasNew = upsertKbDriftEntry(relative, codePaths, commit, date)
        if (wasNew) kbEntriesWritten++
      }
    } else {
      // code→kb: first-match-wins (patterns ordered by specificity in presets)
      const matches = matchAllPatterns(file, patterns)
      if (matches.length > 0) {
        const match = matches[0]
        const kbTarget = resolveKbTarget(match, file)
        const wasNew = upsertCodeDriftEntry(kbTarget, file, commit, date, isShared)
        if (wasNew) codeEntriesWritten++
      }
    }
  }

  const noDrift = codeEntriesWritten === 0 && kbEntriesWritten === 0
  const ownedSubs = submodules.filter(s => !s.isShared).map(s => s.path)
  const sharedSubs = submodules.filter(s => s.isShared).map(s => s.path)
  const subParts = []
  if (ownedSubs.length > 0) subParts.push(`owned: ${ownedSubs.join(', ')}`)
  if (sharedSubs.length > 0) subParts.push(`shared: ${sharedSubs.join(', ')}`)
  const subInfo = subParts.length > 0 ? ` (submodules — ${subParts.join('; ')})` : ''

  return {
    code_entries: codeEntriesWritten,
    kb_entries: kbEntriesWritten,
    submodules_owned: ownedSubs,
    submodules_shared: sharedSubs,
    ...(noDrift
      ? { message: `No drift detected.${subInfo}` }
      : { message: `${codeEntriesWritten} code→KB entry(s) in sync/code-drift.md, ${kbEntriesWritten} KB→code entry(s) in sync/kb-drift.md.${subInfo}` }
    )
  }
}

// ── Phase 2a: code→kb resolved — KB updated ──────────────────────────────────

async function resolveWithSummaries(summaries) {
  const resolved = []

  for (const { kb_target, summary } of summaries) {
    if (!summary || !kb_target) continue
    resolved.push({ direction: 'code→kb', resolution: 'kb-updated', kb_target, summary })
  }

  // Remove fully resolved KB target entries from code-drift.md
  const { entries, header } = readCodeDriftEntries()
  const kbTargets = summaries.map(s => s.kb_target)
  const remaining = entries.filter(e => !kbTargets.includes(e.kbTarget))
  writeCodeDriftEntries(header, remaining)

  appendToDriftLog(resolved)
  return { resolved: resolved.length }
}

// ── Phase 2b: code→kb resolved — code file reverted ──────────────────────────

async function resolveReverted(reverted) {
  const codeFiles = reverted.map(r => r.code_file || r)
  const { entries, header } = readCodeDriftEntries()
  const resolved = []

  // Remove the reverted code file from each entry that contains it
  const updated = entries.map(entry => {
    const before = entry.codeFiles.length
    entry.codeFiles = entry.codeFiles.filter(f => !codeFiles.includes(f.path))
    if (entry.codeFiles.length < before) {
      resolved.push({ direction: 'code→kb', resolution: 'code-reverted', kb_target: entry.kbTarget, code_files: codeFiles })
    }
    return entry
  }).filter(entry => entry.codeFiles.length > 0) // remove empty entries

  writeCodeDriftEntries(header, updated)
  appendToDriftLog(resolved)
  return { reverted: codeFiles.length }
}

// ── Phase 2c: kb→code resolved ────────────────────────────────────────────────

async function resolveKbConfirmed(kb_confirmed) {
  const kbFiles = kb_confirmed.map(r => r.kb_file || r)
  removeFromQueue(KB_DRIFT_PATH, entry => kbFiles.some(f => entry.includes(`## ${f}`)))
  const resolved = kbFiles.map(kb_file => ({ direction: 'kb→code', resolution: 'confirmed', kb_file }))
  appendToDriftLog(resolved)
  return { confirmed: kbFiles.length }
}

// ── code-drift.md parse / serialize ──────────────────────────────────────────

function readCodeDriftEntries() {
  ensureHeader(CODE_DRIFT_PATH, CODE_DRIFT_HEADER)
  const content = fs.readFileSync(CODE_DRIFT_PATH, 'utf8')
  const headerEnd = content.indexOf('\n## ')
  const header = headerEnd === -1 ? content : content.slice(0, headerEnd)
  const entriesStr = headerEnd === -1 ? '' : content.slice(headerEnd + 1)
  const blocks = entriesStr.split(/\n(?=## )/).filter(b => b.trim())

  const entries = blocks.map(block => {
    const headingMatch = block.match(/^## (.+)/)
    const kbTarget = headingMatch ? headingMatch[1].trim() : null
    const hasShared = /\*\*Shared module:\*\*\s*true/.test(block)
    const codeFiles = []
    for (const line of block.split('\n')) {
      const m = line.match(/^\s+-\s+`([^`]+)`\s+—\s+since\s+`([^`]+)`\s+\(([^)]+)\)/)
      if (m) codeFiles.push({ path: m[1], sinceCommit: m[2], sinceDate: m[3] })
    }
    return { kbTarget, codeFiles, hasShared }
  }).filter(e => e.kbTarget)

  return { header, entries }
}

function writeCodeDriftEntries(header, entries) {
  const body = entries.map(entry => {
    const fileLines = entry.codeFiles.map(f => `  - \`${f.path}\` — since \`${f.sinceCommit}\` (${f.sinceDate})`)
    const sharedLine = entry.hasShared ? '\n- **Shared module:** true' : ''
    return `## ${entry.kbTarget}\n\n- **KB target:** \`${entry.kbTarget}\`\n- **Code files:**\n${fileLines.join('\n')}${sharedLine}`
  }).join('\n\n')
  fs.writeFileSync(CODE_DRIFT_PATH, header + (body ? '\n' + body + '\n' : ''), 'utf8')
}

/**
 * Upsert a code file into the entry for a KB target.
 * Returns true if a new entry was created, false if existing was updated or skipped.
 */
function upsertCodeDriftEntry(kbTarget, codeFile, sinceCommit, sinceDate, isShared) {
  const { header, entries } = readCodeDriftEntries()
  const existing = entries.find(e => e.kbTarget === kbTarget)

  if (existing) {
    if (!existing.codeFiles.some(f => f.path === codeFile)) {
      existing.codeFiles.push({ path: codeFile, sinceCommit, sinceDate })
      if (isShared) existing.hasShared = true
      writeCodeDriftEntries(header, entries)
    }
    return false // entry already existed
  }

  entries.push({ kbTarget, codeFiles: [{ path: codeFile, sinceCommit, sinceDate }], hasShared: !!isShared })
  writeCodeDriftEntries(header, entries)
  return true // new entry created
}

// ── kb-drift.md upsert ────────────────────────────────────────────────────────

/**
 * Upsert a KB file entry in kb-drift.md.
 * Returns true if new entry created.
 */
function upsertKbDriftEntry(kbFile, codeAreas, sinceCommit, sinceDate) {
  ensureHeader(KB_DRIFT_PATH, KB_DRIFT_HEADER)
  const content = fs.readFileSync(KB_DRIFT_PATH, 'utf8')

  // Already tracked — don't duplicate (git diff since..HEAD covers new commits)
  if (content.includes(`## ${kbFile}`)) return false

  const areaLines = codeAreas.map(p => `  - \`${p}\``).join('\n')
  const entry = `\n## ${kbFile}\n\n- **KB file:** \`${kbFile}\`\n- **Code areas to review:**\n${areaLines}\n- **Since:** \`${sinceCommit}\` (${sinceDate})\n`
  fs.appendFileSync(KB_DRIFT_PATH, entry)
  return true
}

// ── Shared queue helpers ──────────────────────────────────────────────────────

function ensureHeader(filePath, header) {
  if (!fs.existsSync(filePath)) {
    fs.mkdirSync(path.dirname(filePath), { recursive: true })
    fs.writeFileSync(filePath, header, 'utf8')
  } else {
    const content = fs.readFileSync(filePath, 'utf8')
    if (!content.startsWith('<!-- AUTO-GENERATED')) {
      fs.writeFileSync(filePath, header + content, 'utf8')
    }
  }
}

function removeFromQueue(filePath, matchFn) {
  if (!fs.existsSync(filePath)) return []
  const content = fs.readFileSync(filePath, 'utf8')
  const headerEnd = content.indexOf('\n## ')
  const header = headerEnd === -1 ? content : content.slice(0, headerEnd)
  const entriesStr = headerEnd === -1 ? '' : content.slice(headerEnd + 1)
  const entries = entriesStr.split(/\n(?=## )/).filter(e => e.trim())
  const removed = []
  const remaining = []
  for (const entry of entries) {
    if (matchFn(entry)) removed.push(entry)
    else remaining.push(entry)
  }
  const newContent = header + (remaining.length > 0 ? '\n' + remaining.join('\n') : '')
  fs.writeFileSync(filePath, newContent, 'utf8')
  return removed
}

function appendToDriftLog(entries) {
  if (!fs.existsSync(DRIFT_LOG_DIR)) fs.mkdirSync(DRIFT_LOG_DIR, { recursive: true })
  const logPath = getDriftLogPath()
  ensureHeader(logPath, DRIFT_LOG_HEADER)
  const date = new Date().toISOString().split('T')[0]
  let block = ''
  for (const entry of entries) {
    block += `\n## ${date} · RESOLVED · ${entry.direction}\n`
    if (entry.kb_target) block += `\n- **KB target:** \`${entry.kb_target}\``
    if (entry.kb_file) block += `\n- **KB file:** \`${entry.kb_file}\``
    if (entry.code_file) block += `\n- **Code file:** \`${entry.code_file}\``
    if (entry.code_files) block += `\n- **Code files:** ${entry.code_files.map(f => `\`${f}\``).join(', ')}`
    block += `\n- **Resolution:** ${entry.resolution}`
    if (entry.summary) block += `\n- **Summary:** ${entry.summary}`
    block += '\n'
  }
  if (block) fs.appendFileSync(logPath, block)
}

// ── kb→code reverse mapping ───────────────────────────────────────────────────

function reverseMapKbTarget(kbRelative, patterns) {
  const codePaths = []
  for (const pattern of patterns) {
    if (matchesKbTargetPattern(pattern.kb_target, kbRelative)) {
      codePaths.push(...(pattern.paths || []))
    }
  }
  return [...new Set(codePaths)]
}

function matchesKbTargetPattern(patternTarget, actualPath) {
  const regexStr = patternTarget
    .replace(/\./g, '\\.')
    .replace(/\{name\}/g, '[^/]+')
  try {
    return new RegExp(`^${regexStr}$`).test(actualPath)
  } catch {
    return patternTarget === actualPath
  }
}

function isKbContentFile(file) {
  if (!file.startsWith('knowledge/')) return false
  const rel = file.replace(/^knowledge\//, '')
  if (rel.startsWith('_') || rel.startsWith('sync/') || rel.startsWith('exports/') || rel.startsWith('assets/')) return false
  return file.endsWith('.md')
}

// ── git helpers ───────────────────────────────────────────────────────────────

/**
 * Resolve the best "last sync" ref for drift comparison.
 * Graduated fallback: upstream tracking → remote branch → closest parent branch → null.
 * Returns null when no reliable baseline exists (caller should skip with warning).
 */
async function resolveLastSyncRef(git, remote) {
  // 1. Try upstream tracking ref (e.g. origin/main) — covers all unpushed commits
  try {
    const upstream = await git.raw(['rev-parse', '--abbrev-ref', '@{upstream}'])
    if (upstream && upstream.trim()) return upstream.trim()
  } catch { /* no upstream configured */ }

  // Validate that the requested remote exists in this repo.
  // Submodules may have different remote names than the parent repo.
  let validRemote = remote
  if (remote) {
    try {
      const remotes = (await git.raw(['remote'])).split('\n').filter(r => r.trim())
      if (!remotes.includes(remote)) {
        const cwd = git._executor?.cwd || 'unknown'
        process.stderr.write(`[kb-drift] warning: remote '${remote}' not found in ${cwd} — available remotes: ${remotes.join(', ') || 'none'}. Skipping drift detection.\n`)
        process.stderr.write(`[kb-drift]   fix: git -C ${cwd} remote rename <correct> ${remote}\n`)
        validRemote = null
      }
    } catch { validRemote = null }
  }

  // 2. Try <remote>/<current-branch> — branch exists on remote but no -u was used
  if (validRemote) {
    try {
      const branch = (await git.raw(['symbolic-ref', '--short', 'HEAD'])).trim()
      const remoteBranch = `${validRemote}/${branch}`
      await git.raw(['rev-parse', remoteBranch])
      return remoteBranch
    } catch { /* remote branch doesn't exist yet */ }
  }

  // 3. Find closest parent branch on remote — new branch, first push
  //    Tries all remote branches, picks the one whose merge-base is closest to HEAD.
  //    For main->dev->feature, this finds dev (not main) as the parent.
  if (validRemote) {
    try {
      const remoteBranches = (await git.raw(['for-each-ref', '--format=%(refname:short)', `refs/remotes/${validRemote}/`]))
        .split('\n')
        .filter(b => b.trim() && !b.includes('/HEAD'))
      let closest = null
      let minDistance = Infinity
      for (const rb of remoteBranches) {
        try {
          const mb = (await git.raw(['merge-base', 'HEAD', rb])).trim()
          if (!mb) continue
          const count = parseInt((await git.raw(['rev-list', '--count', `${mb}..HEAD`])).trim(), 10)
          if (count < minDistance) {
            minDistance = count
            closest = mb
          }
        } catch { /* skip branches with no common ancestor */ }
      }
      if (closest) {
        if (minDistance > 20) {
          process.stderr.write(`[kb-drift] hint: ${minDistance} commits since parent branch — consider pulling/rebasing to reduce drift noise\n`)
        }
        return closest
      }
    } catch { /* no remote branches available */ }
  }

  // 4. No reliable baseline — return null (caller skips with warning)
  return null
}

async function getChangedFiles(git, ref) {
  try {
    const result = await git.diff(['--name-only', ref, 'HEAD'])
    return result.split('\n').filter(f => f.trim())
  } catch {
    // Fallback for edge cases (detached HEAD, shallow clone, etc.)
    try {
      const result = await git.diff(['--name-only', '4b825dc642cb6eb9a060e54bf899d15f7dcb6820', 'HEAD'])
      return result.split('\n').filter(f => f.trim())
    } catch {
      return []
    }
  }
}

async function detectSubmodules() {
  const gitmodulesPath = path.join(process.cwd(), '.gitmodules')
  if (!fs.existsSync(gitmodulesPath)) return []
  const content = fs.readFileSync(gitmodulesPath, 'utf8')
  const submodules = []
  const blocks = content.split(/(?=\[submodule\s+"[^"]+"\])/).filter(b => b.trim())
  for (const block of blocks) {
    const nameMatch = block.match(/\[submodule\s+"([^"]+)"\]/)
    const pathMatch = block.match(/path\s*=\s*(.+)/)
    if (!nameMatch || !pathMatch) continue
    const name = nameMatch[1].trim()
    const subPath = pathMatch[1].trim()
    const fullPath = path.join(process.cwd(), subPath)
    const isShared = /kb-shared\s*=\s*true/.test(block)
    if (fs.existsSync(fullPath)) submodules.push({ name, path: subPath, fullPath, isShared })
  }
  return submodules
}

