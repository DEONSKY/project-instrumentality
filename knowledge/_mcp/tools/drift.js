// Merge protocol guidance shared between both queue headers. The files are
// merged with `merge=union` (see .gitattributes): different entries coalesce
// cleanly, same-entry conflicts are real and intentional, and duplicate
// baseline lines are collapsed by the post-merge hook (or manually — keep
// the descendant SHA).
const MERGE_PROTOCOL_NOTE = `> **On merge conflicts:** queue files use \`merge=union\` — different entries from different branches coalesce automatically. If the same entry appears twice, dedupe by merging the two: keep the earlier \`Since\` commit (the true drift anchor) and the later \`Latest\` commit (the most recent touch). The post-merge git hook collapses duplicate \`<!-- baseline: -->\` lines automatically; if it didn't run, keep whichever SHA is the descendant of the other.`

